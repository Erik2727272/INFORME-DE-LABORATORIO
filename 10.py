#CUESTIONARIO: ¿Cuáles son las principales ventajas de la programación recursiva en
# Python?


#1.La eficiencia de la recursividad reside en el hecho de que se puede usar para resolver problemas de difícil solución iterativa.

#2.Algunos problemas son mas sencillos de implementar usando la recursividad.

#3.Presenta una facilidad para comprobar y verificar que la solución es correcta.

#4.Soluciones a problemas complejos de una manera más fácil, simple, clara y elegante.

#5.No es necesario definir la secuencia de pasos exacta para resolver el problema.

#6.Podemos considerar que “tenemos resuelto el problema” (de menor tamaño).


#La recursividad es una manera fácil de resolver  y aplicable a ejercicios donde hay muchas iteraciones, y este se usa para 
#evitar  problemas de memoria debido a la alta cantidad de variables que se deben  guardar y la sobrecarga de métodos, por ello es bueno
# acudir a la recursividad cuando es mas conveniente.